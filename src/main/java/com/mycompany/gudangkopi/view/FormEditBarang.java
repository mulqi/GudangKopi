    private void cbGradeKopiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbGradeKopiActionPerformed
        // TODO add your handling code here:
        String gradeTerpilih = cbGradeKopi.getSelectedItem().toString();

        java.util.List<com.mycompany.gudangkopi.model.InventarisKopi> listKopi = kopiController.getAllInventaris();
    
        for (com.mycompany.gudangkopi.model.InventarisKopi k : listKopi) {
            if (k.getGradeKopi().equalsIgnoreCase(gradeTerpilih)) {
                // update tampilan teks
                txtHarga.setText(String.format("%.0f", k.getHargaPerKg()));
                txtStok.setText(String.format("%.0f", k.getStok()));

                this.idBarangTerpilih = String.valueOf(k.getIdBarang());
                break;
            }
        }
    }//GEN-LAST:event_cbGradeKopiActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtHarga.getText().trim().isEmpty() || txtStok.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Harga dan Stok tidak boleh kosong!", 
                    "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (idBarangTerpilih == null || idBarangTerpilih.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Silakan pilih Grade Kopi terlebih dahulu!", 
                    "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int idBarang = Integer.parseInt(idBarangTerpilih);
            double hargaBaru = Double.parseDouble(txtHarga.getText().trim());
            double stokBaru = Double.parseDouble(txtStok.getText().trim());
            String gradeTerpilih = cbGradeKopi.getSelectedItem().toString();

            com.mycompany.gudangkopi.model.InventarisKopi kopiEdit = 
                new com.mycompany.gudangkopi.model.InventarisKopi(idBarangTerpilih, gradeTerpilih, hargaBaru, stokBaru);

            boolean berhasil = kopiController.updateInventaris(kopiEdit); 

            if (berhasil) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data Inventaris Kopi berhasil diperbarui!");
    
                java.awt.Window[] windows = java.awt.Window.getWindows();
                for (java.awt.Window window : windows) {
                    if (window.getClass().getName().equals("com.mycompany.gudangkopi.view.dashboard")) {
                        try {
                            java.lang.reflect.Method method = window.getClass().getMethod("loadDataTabel");
                            method.invoke(window);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memperbarui data di database.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Input Harga dan Stok harus berupa angka!", 
                    "Format Salah", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

