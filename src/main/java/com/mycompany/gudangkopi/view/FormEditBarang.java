    public void siapkanDataForm(String id, String grade, String harga, String stok) {
        this.idBarangTerpilih = id;
        
        cbGradeKopi.setSelectedItem(grade);
        
        String hargaMurni = harga.replace("Rp ", "").trim();
        String stokMurni = stok.replace(" Kg", "").trim();
        
        txtHarga.setText(hargaMurni);
        txtStok.setText(stokMurni);
    }
    
    private final com.mycompany.gudangkopi.controller.InventarisKopiController kopiController = 
            new com.mycompany.gudangkopi.controller.InventarisKopiController();

    public void gradeKopi() {
        cbGradeKopi.removeAllItems();
        java.util.List<com.mycompany.gudangkopi.model.InventarisKopi> listKopi = kopiController.getAllInventaris();
        
        for (com.mycompany.gudangkopi.model.InventarisKopi k : listKopi) {
            boolean sudahAda = false;
            for (int i = 0; i < cbGradeKopi.getItemCount(); i++) {
                if (cbGradeKopi.getItemAt(i).equals(k.getGradeKopi())) {
                    sudahAda = true;
                    break;
                }
            }
            if (!sudahAda && k.getGradeKopi() != null) {
                cbGradeKopi.addItem(k.getGradeKopi());
            }
        }
    }

